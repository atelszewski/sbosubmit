#include <string>
#include <ostream>
#include "sbo_util.h"
#include "sbo_classlog.h"

/******************************************************************************/
sbo_classlog_t::sbo_classlog_t(string const &pcname) :
  class_name(pcname)
/******************************************************************************/
{
}

/******************************************************************************/
ostream &sbo_classlog_t::imsg(int const vl) const
/******************************************************************************/
{
  return ::imsg(vl) << class_name << ": ";
}

/******************************************************************************/
ostream &sbo_classlog_t::wmsg(void) const
/******************************************************************************/
{
  return ::wmsg() << class_name << ": ";
}

/******************************************************************************/
ostream &sbo_classlog_t::emsg(void) const
/******************************************************************************/
{
  return ::emsg() << class_name << ": ";
}
